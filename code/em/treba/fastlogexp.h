inline PROB log1plus_table_interp(PROB x) {
    int index;
    PROB val1, val2, w ;
    index = -(int)(x * LOGPLUSTABLETICKS);
    w = -(x * LOGPLUSTABLETICKS) - index;
    val1 = g_logplustable[index++];
    val2 = g_logplustable[index];
    return(val1 + w * (val2-val1));
}

